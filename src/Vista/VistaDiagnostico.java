    private void btnEnfermarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnfermarActionPerformed
        try{
            if(validarDuplicado()){
                cargarModeloCombo(modelo);
                cargarMedicamento(lstListaEnfermedad.getSelectedValue());
                lblError.setText("");
            }           
        }catch(NullPointerException npe){
            lblError.setText("no ha seleccionado nada");
        }
    }//GEN-LAST:event_btnEnfermarActionPerformed

    private void btnAsociarEnfermedadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsociarEnfermedadActionPerformed
        try{    
            for(int i=0;i<cmbPadecimiento.getItemCount();i++){
                if(!cmbPadecimiento.getItemAt(i).equals("Seleccionar")){                    
                    pacienteEnfermo.adicionarPacienteEnfermo(Integer.parseInt(lblCedula.getText()),(String)cmbPadecimiento.getItemAt(i));            
                }
            }
            JOptionPane.showMessageDialog(null,"Datos de enfermedades adicionados","informacion",JOptionPane.INFORMATION_MESSAGE);
            validarEstado();
            lblError.setText("");
        }catch(NullPointerException npe){
            lblError.setText("null pointer");
        }
    }//GEN-LAST:event_btnAsociarEnfermedadActionPerformed

