    private void deshabilitarVentana(){
        btnAsociarEnfermedad.setEnabled(false);
        btnEnfermar.setEnabled(false);
        btnFormular.setEnabled(false);
        btnGuardarCambios.setEnabled(false);
        cmbPadecimiento.setEnabled(false);
        lstListaEnfermedad.setEnabled(false);
        JOptionPane.showMessageDialog(null,"Paciente Formulado");       
    }
    
    public void cargarPaciente(int cedula,int edad,String nombre,String genero,String foto){
        lblCedula.setText(Integer.toString(cedula));
        lblEdad.setText(Integer.toString(edad));
        lblNombre.setText(nombre);
        lblGenero.setText(genero);        
        cargarFoto(foto);
         
    }

    private void cargarFoto(String foto) {
        repaint();
        ImageIcon img=new ImageIcon(foto);
        Icon icono=new ImageIcon(img.getImage().getScaledInstance(50,45,Image.SCALE_DEFAULT));
        lblFoto.setIcon(icono);
        
    }
    
    private void validarSalud(){
        if(lblEstadoPaciente.getText().equals("Sano")){
            btnFormular.setEnabled(false);
            txtaDescripcion.setEnabled(false);
            txtFecha.setEnabled(false);            
        }else{
            btnFormular.setEnabled(true);
            txtaDescripcion.setEnabled(true);
            txtFecha.setEnabled(true);            
        }
    }
    
    private void validarEstado(){        
        if(!cmbPadecimiento.getSelectedItem().equals("Seleccionar")){
            lblEstadoPaciente.setText("Enfermo");
        }
        validarSalud();
    }
    
