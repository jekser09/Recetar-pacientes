    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try{    
            st=new StringTokenizer(lstPacientes.getSelectedValue(),",");
            st.nextToken();
            int ced=Integer.parseInt(st.nextToken());
            estadoBotones(false, true, true);
            estadoCampos(true,true,true,true);
            txtNombre.setText(control.buscarPaciente(ced).getNombre());
            txtCedula.setText(Integer.toString(control.buscarPaciente(ced).getCedula()));
            txtEdad.setText(Integer.toString(control.buscarPaciente(ced).getEdad()));
            cmbGenero.setSelectedItem(control.buscarPaciente(ced).getGenero());
            rutaFoto=control.buscarPaciente(ced).getFoto();
            foto(control.buscarPaciente(ced).getFoto());
            add=false;
            pos=control.buscarPaciente(ced).getId()-1;
            lblError.setText("");
        }catch(NullPointerException np){
            lblError.setText("Ningun paciente seleccionado: "+np.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

