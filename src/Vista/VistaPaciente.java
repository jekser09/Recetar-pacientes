    private void btnDiagnosticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiagnosticoActionPerformed
        vistaFormula=new VistaDiagnostico();
        vistaFormula.setSize(principal.getWidth(),principal.getHeight());
        enfermedadCtrl=new EnfermedadCtrl();
        try{
            st=new StringTokenizer(lstPacientes.getSelectedValue(),",");            
            st.nextToken();
            int ced=Integer.parseInt(st.nextToken());           
            vistaFormula.cargarPaciente(control.buscarPaciente(ced).getCedula(),control.buscarPaciente(ced).getEdad(),
                                        control.buscarPaciente(ced).getNombre(),control.buscarPaciente(ced).getGenero(),
                                        control.buscarPaciente(ced).getFoto());
            vistaFormula.cargarEnfermedades(enfermedadCtrl.modeliLista());            
            vistaFormula.setVisible(true);            
            principal.setContentPane(vistaFormula);
            principal.repaint();            
            lblError.setText("");
        }catch(NullPointerException npe){
            lblError.setText("no ha seleccionado nada");
        }
        
        
    }//GEN-LAST:event_btnDiagnosticoActionPerformed


