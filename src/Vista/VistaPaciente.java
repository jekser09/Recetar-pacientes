    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(validarCampos()){    
            if(add){
                if(control.verificarDuplicados(Integer.parseInt(txtCedula.getText()))){
                    control.adicionarPacientes(Integer.parseInt(txtCedula.getText()),txtNombre.getText(),Integer.parseInt(txtEdad.getText()),
                                               (String) cmbGenero.getSelectedItem(),rutaFoto);
                    listarPacientes();
                    limpiarCampos();
                    estadoBotones(true,false,false);
                    estadoCampos(false,false,false,false);
                    listarPacientes();
                    cargarPrimeraImagen();
                    btnGuardarCambios.setEnabled(true);
                }
            }else{
                control.getListaPacientes().get(pos).setCedula(0);
                if(control.verificarDuplicados(Integer.parseInt(txtCedula.getText()))){
                    control.modificarPaciente(pos,Integer.parseInt(txtCedula.getText()), txtNombre.getText()
                                              ,Integer.parseInt(txtEdad.getText()), (String) cmbGenero.getSelectedItem(),
                                               rutaFoto);                    
                    limpiarCampos();
                    estadoBotones(true,false,false);
                    estadoCampos(false,false,false,false);
                    listarPacientes();
                    cargarPrimeraImagen();
                    btnGuardarCambios.setEnabled(true);
                    add=true;
                }
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

