    private void estadoLista(){
        if(!control.getListaPacientes().isEmpty()){
            btnInfo.setEnabled(true);
            btnDiagnostico.setEnabled(true);
            btnEditar.setEnabled(true);
            btnEliminar.setEnabled(true);
            lstPacientes.setEnabled(true);
        }else{
            btnInfo.setEnabled(false);
            btnDiagnostico.setEnabled(false);
            btnEditar.setEnabled(false);
            btnEliminar.setEnabled(false);
            lstPacientes.setEnabled(false);
        }      
    }

    private void foto(String f) {       
        ImageIcon img=new ImageIcon(f);        
        Icon icono=new ImageIcon(img.getImage().getScaledInstance(lblFoto.getWidth(),lblFoto.getHeight(),Image.SCALE_DEFAULT));        
        lblFoto.setIcon(icono);        
        repaint();       
    }
    
